//Strings
let nombre = "Edgar Plazola";
console.log(nombre);
nombre = "Valeria Magaña";
console.log(nombre);
nombre = "Tía May";
console.log(typeof nombre);

//Booleanos
let isMarvel = true;
console.log(isMarvel);
console.log(typeof isMarvel);

//Números
let edad = 33;
console.log(edad);
console.log(typeof edad);

//Nulls
let isNull = null;
console.log(isNull);
console.log(typeof isNull);

//Symbol - identificar propiedades de forma única
let symbol1 = Symbol('a');
console.log(symbol1);
console.log(typeof symbol1);
let symbol2 = Symbol('a');
console.log(symbol2);
console.log(typeof symbol2);

console.log(symbol1 === symbol2);